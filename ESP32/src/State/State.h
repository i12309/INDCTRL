#pragma once

#include <Arduino.h>

// Базовый класс простой машины состояний прошивки.
class State {
public:
    // Типы системных состояний, по которым можно диагностировать текущий режим.
    enum class Type {
        // Начальная загрузка, проверка Wi-Fi и ожидание касания Load.
        Boot,
        // Ожидание действий пользователя после загрузки.
        Idle,
        // Системная ошибка.
        Error,
    };

    // Запомнить тип конкретного состояния.
    explicit State(Type type) : type_(type) {}
    // Виртуальный деструктор нужен, потому что состояния удаляются через State*.
    virtual ~State() = default;

    // Вернуть тип состояния для диагностики.
    Type type() const { return type_; }

    // Создать первое состояние прошивки.
    static void init();
    // Выполнить tick текущего состояния и переключиться, если оно вернуло новое.
    static void process();
    // Принудительно установить новое состояние и вызвать его onEnter().
    static void set(State* next);
    // Вернуть активное состояние или nullptr до инициализации.
    static State* current();

protected:
    // Хук входа в состояние; переопределяется там, где нужно обновить UI.
    virtual void onEnter() {}
    // Один шаг логики состояния; возвращает this или новое состояние.
    virtual State* tick() = 0;

private:
    // Тип текущего экземпляра состояния.
    Type type_;
    // Глобальный указатель на активное состояние машины.
    static State* current_;
};
