#pragma once

// Корневой объект приложения: инициализирует сервисы, экран и машину состояний.
class App {
public:
    // Общий контекст приложения для флагов, которые нужны разным слоям.
    struct Context {
        // Признак, что init() уже выполнен и повторно запускать инициализацию нельзя.
        bool initialized = false;
    };

    // Создает экземпляр приложения и регистрирует singleton.
    App();

    // Вернуть текущий singleton приложения.
    static App* instance();
    // Вернуть общий контекст приложения.
    static Context& context();

    // Инициализировать логирование, сервисы, UI и начальное состояние.
    void init();
    // Выполнить один шаг обработки сервисов, UI и бизнес-состояний.
    void process();

private:
    // Указатель на единственный экземпляр приложения.
    static App* instance_;
    // Runtime-контекст приложения.
    Context context_;
};
